#include "libft.h"

int64_t	ft_exponent(int32_t a, int32_t b)
{
	int64_t	ans;

	ans = 1;
	while (b > 0)
	{
		if (b & 1)
			ans = (ans * a);
		a = a * a;
		b = b >> 1;
	}
	return (ans);
}
