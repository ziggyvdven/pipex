#include "lst.h"

int32_t	to_int(void *p)
{
	return (*(int32_t *)p);
}

int32_t	*to_ints(void *p)
{
	return ((int32_t *)p);
}

uint32_t	to_uint(void *p)
{
	return (*(uint32_t *)p);
}
