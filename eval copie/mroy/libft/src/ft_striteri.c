#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	unsigned int	s_l;
	unsigned int	i;

	s_l = (unsigned int)ft_strlen(s);
	i = 0;
	while (i < s_l)
	{
		(*f)(i, &s[i]);
		i++;
	}
}
